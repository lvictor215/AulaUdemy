'''
Três meios de colocar aspas em prints (ou strings)

1ª Digitando aspas duplas fora para dentro usar aspas simples. Exemplo:
'''
print("Quero colocar esse 'nome' entre aspas simples")

#2ª, Colocando aspas simples fora para dentro usar aspas duplas. Exemplo:

print('Quero colocar esse "nome" entre aspas duplas')

#3ª colocando o caractere "\" antes da aspas para falar que a aspas é apenas texto
print("Quero colocar esse \"nome\" entre aspas")

